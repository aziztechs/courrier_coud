                <h1>Aide - Système de Gestion de Courrier</h1>
                <p>
                    Ce projet de gestion de courrier a pour objectif de faciliter le suivi, l'imputation, et la gestion des courriers au sein de l'organisation. 
                    Voici un aperçu des fonctionnalités disponibles dans cette application :
                </p>
                <ul>
                    <li><strong>Gestion des courriers :</strong> Suivi et gestion des courriers entrants et sortants avec des champs tels que le numéro de courrier, la date, l'objet, et la nature.</li>
                    <li><strong>Imputation des courriers :</strong> Attribution des courriers à différents départements avec la possibilité de définir des instructions spécifiques.</li>
                    <li><strong>Recherche et tri :</strong> Recherche avancée des courriers par numéro, date, département, ou objet. Les résultats peuvent être triés par date, avec les courriers les plus récents affichés en premier.</li>
                    <li><strong>Gestion des utilisateurs :</strong> Gestion des rôles des utilisateurs avec différents niveaux d'accès (département, direction, b_courrier) et la possibilité de diviser le rôle "département" en sous-rôles.</li>
                    <li><strong>Suivi des imputations :</strong> Visualisation des imputations de courriers avec pagination et filtres pour faciliter la navigation.</li>
                    <li><strong>Suppression et modification :</strong> Possibilité de supprimer ou de modifier les informations des courriers en fonction des droits de l'utilisateur.</li>
                </ul>
                <p>
